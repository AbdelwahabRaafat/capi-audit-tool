let currentLang = localStorage.getItem('sig_lang') || 'en';

/* Shorthand translator */
function t(key) {
  const lang = TRANSLATIONS[currentLang] || TRANSLATIONS.en;
  return lang[key] !== undefined ? lang[key] : (TRANSLATIONS.en[key] || key);
}

/* Apply translations to the DOM */
function applyTranslations() {
  var lang = TRANSLATIONS[currentLang] || TRANSLATIONS.en;

  /* Direction */
  document.documentElement.setAttribute('dir', lang.dir);
  document.documentElement.setAttribute('lang', currentLang);

  /* data-i18n — set textContent */
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.dataset.i18n;
    if (lang[key] !== undefined) el.textContent = lang[key];
  });

  /* data-i18n-html — set innerHTML */
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.dataset.i18nHtml;
    if (lang[key] !== undefined) el.innerHTML = lang[key];
  });

  /* data-i18n-ph — set placeholder */
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    var key = el.dataset.i18nPh;
    if (lang[key] !== undefined) el.placeholder = lang[key];
  });

  /* Update dropdown: current label + active option */
  document.querySelectorAll('.lang-current').forEach(function(el) {
    el.textContent = currentLang.toUpperCase();
  });
  document.querySelectorAll('.lang-option').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });
}

/* ── Dropdown toggle ── */
function toggleLangMenu(e) {
  e.stopPropagation();
  var sw = e.currentTarget.closest ? e.currentTarget.closest('.lang-switcher') : document.querySelector('.lang-switcher');
  if (!sw) return;
  var isOpen = sw.classList.contains('open');
  /* Close all first */
  document.querySelectorAll('.lang-switcher.open').forEach(function(el) { el.classList.remove('open'); });
  if (!isOpen) sw.classList.add('open');
}
function closeLangMenu() {
  document.querySelectorAll('.lang-switcher.open').forEach(function(el) { el.classList.remove('open'); });
}
document.addEventListener('click', closeLangMenu);

/* Set language and persist */
function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('sig_lang', lang);
  applyTranslations();
  closeLangMenu();

  /* Re-render audit results if on results step */
  if (typeof state !== 'undefined' && state.currentStep === 5) {
    computeAndRenderResults();
  }

  /* Page-specific hook — set window._onLangChange to extend without overriding */
  if (typeof window._onLangChange === 'function') {
    window._onLangChange(lang);
  }
}

/* Run on page load */
document.addEventListener('DOMContentLoaded', applyTranslations);
